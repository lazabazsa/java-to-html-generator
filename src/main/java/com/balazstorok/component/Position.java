package com.balazstorok.component;

/**
 * Enum for handling the positions of each component.
 * The position tells the container where should the currently added component be placed.
 *
 * Created by Balazs Torok on 11/02/17.
 */
public enum Position {
	/**
	 * BESIDE: Beside the component which was added before.
	 * NEXT_LINE: Into the next line.
	 */
	BESIDE,
	NEXT_LINE
}
